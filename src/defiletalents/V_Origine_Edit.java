    /**
     * Méthode utilisée lorsque l'on appuie sur le bouton 'Valider'.
     *
     * @param evt
     */
    private void btValiderActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btValiderActionPerformed
    {//GEN-HEADEREND:event_btValiderActionPerformed
        boolean error = false;
        String code = edCode.getText();
        String libelle = edLibelle.getText();

        if (code.equals("") || libelle.equals("")) {
            error = true;
        }

        if (!error) {
            if (!checkOrigine(code)) {
                controller.updateOrigine(id, code, libelle);
                controller.getOrigineAllFrame().setVisible(true);
                setVisible(false);
            }
            else {
                lbWarning.setText("Le code existe déjà.");
            }
        }
        else {
            lbWarning.setText("Tous les champs sont requis.");
        }
    }//GEN-LAST:event_btValiderActionPerformed

