    @Override
    public void setVisible(boolean b) {
        if (b) {
            setLocationRelativeTo(controller.getOrigineAllFrame());
            lbWarning.setText("");
        }
        super.setVisible(b);
    }

    /**
     * Met à jour les valeurs utilisées par la vue.
     *
     * @param id
     * @param code
     * @param libelle
     * @param mode
     */
    public void setValues(int id, String code, String libelle, int mode) {
        this.id = id;
        this.mode = mode;

        switch (mode) {
            case V_Main.MODE_AJOUT:
                setTitle("Ajout d'une nouvelle origine");
                edCode.setText("");
                edLibelle.setText("");
                break;
            case V_Main.MODE_EDITION:
                setTitle("Edition de l'origine " + id);
                edCode.setText(code);
                edLibelle.setText(libelle);
                break;
            default:
                setTitle("Consultation de l'origine " + id);
                edCode.setText(code);
                edLibelle.setText(libelle);
                edCode.setEditable(false);
                edLibelle.setEditable(false);
                btValider.setVisible(false);
                btAnnuler.setText("Fermer");
                break;
        }
    }
    /**
     * Vérifie que le code n'existe pas deja dans la DB
     * @param code
     * @return 
     */
    private boolean checkOrigine(String code) {
        boolean error = false;
        for (M_Origine o : controller.getOrigines()) {
            if (code.equals(o.getCode())) {
                error = true;
                break;
            }
        }
        return error;
    }

